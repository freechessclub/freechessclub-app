import { legalEnPassant } from './chess-helper';

export function zobrist128(fen: string): bigint {
  const fields = fen.split(' ');

  let hash = 0n;

  // pieces
  let square = 0;

  for (const c of fields[0]) {
    if (c === "/") 
      continue;

    if (c >= "1" && c <= "8") {
      square += Number(c);
      continue;
    }

    const color = c === c.toUpperCase() ? 1 : 0;
    const role = "pnbrqk".indexOf(c.toLowerCase()) + 1;
    const pieceIndex = (role - 1) * 2 + color;
    
    const file = square & 7;
    const rank = square >> 3;
    const shakmatySquare = (7 - rank) * 8 + file;
    hash ^= PIECE_MASKS[64 * pieceIndex + shakmatySquare];

    square++;
  }

  // side to move
  if(fields[1] === "w") 
    hash ^= WHITE_TURN_MASK;

  // castling
  for(const c of fields[2]) {
    switch (c) {
      case "K": hash ^= CASTLING_RIGHT_MASKS[0]; break;
      case "Q": hash ^= CASTLING_RIGHT_MASKS[1]; break;
      case "k": hash ^= CASTLING_RIGHT_MASKS[2]; break;
      case "q": hash ^= CASTLING_RIGHT_MASKS[3]; break;
    }
  }

  // en passant
  if(fields[3] !== "-" && legalEnPassant(fen)) {
    const file = fields[3].charCodeAt(0) - 97;
    hash ^= EN_PASSANT_FILE_MASKS[file];
  }

  return hash;
}

